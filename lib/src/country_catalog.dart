@freezed
class Country with _$Country {
  const factory Country({
    @JsonKey() required String numCode,
    @JsonKey() required String alphaCode2,
    @JsonKey() required String alphaCode3,
    @JsonKey() required String name,
    @JsonKey() required String nationality,
    @JsonKey() required String code,
    @Default(false) @JsonKey(defaultValue: false) bool stripe,
  }) = _Country;

  factory Country.fromJson(Map<String, dynamic> json) => _$CountryFromJson(json);
  static AssetImage imageOf(Country country, {String package = 'country_catalog'}) =>
      AssetImage('flags/${country.alphaCode2.toLowerCase()}.png', package: package);

  static String localize(BuildContext context, Country country) =>
      CountryCatalogLocalizations.of(context)?.getString('${country.alphaCode2.toLowerCase()}CountryName') ??
      country.name;
}

class CountryConverter implements JsonConverter<Country?, String?> {
  const CountryConverter();

  @override
  Country? fromJson(String? json) => json != null ? CountryCatalog.fromAlphaCode(json) : null;

  @override
  String? toJson(Country? object) => object?.alphaCode2;
}

